package latipe.search.constants;

/**
 * Defines constant action types used within the search service.
 * These constants are used to indicate the type of operation being performed on records.
 */
public final class Action {

    /**
     * Represents an update operation
     */
    public static final String UPDATE = "u";
    
    /**
     * Represents a create operation
     */
    public static final String CREATE = "c";
    
    /**
     * Represents a delete operation
     */
    public static final String DELETE = "d";
    
    /**
     * Represents a ban operation
     */
    public static final String BAN = "b";
}
